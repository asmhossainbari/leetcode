class Solution(object):
    def isValidSudoku(self, board):
        """
        :type board: List[List[str]]
        :rtype: bool
        """
        row_set = [set() for i in range(9)]
        col_set = [set() for i in range(9)]
        box_set = [set() for i in range(9)]
        for r in range(9):
            for c in range(9):
                ch = board[r][c]
                box_index = 3 * (r // 3) + (c // 3)
                if ch == '.':
                    continue
                if ch in row_set[r] or ch in col_set[c] or ch in box_set[box_index]:
                    return False
                
                row_set[r].add(ch)
                col_set[c].add(ch)
                box_set[box_index].add(ch)

        return True


